#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int			i;
	int			final;
	int			sign;

	i = 0;
	sign = 1;
	final = 0;
	while (nptr[i] == ' ' || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
		{
			sign = -1;
		}
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		final = 10 * final + (nptr[i] - '0');
		i++;
	}
	return (sign * final);
}
